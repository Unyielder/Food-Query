class Food:
    def __init__(self, food_name, measure, unit, grams, calories, proteins,
                 fat, sat_fat, fiber, carbs, category):
        self.food_name = food_name
        self.measure = measure
        self.unit = unit
        self.grams = grams
        self.calories = calories
        self.proteins = proteins
        self.fat = fat
        self.sat_fat = sat_fat
        self.fiber = fiber
        self.carbs = carbs
        self.category = category

